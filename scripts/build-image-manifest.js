/**
 * Сканирует public/images/: папки = id постов, внутри фото 1, 2, 3...
 * Пишет мап в src/data/image-manifest.generated.ts (без отдельного JSON).
 * Запуск: npm run update-images или при npm run build
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

const IMAGE_EXT = /\.(jpg|jpeg|png|gif|webp)$/i
const publicImages = path.join(__dirname, '..', 'public', 'images')
const outPath = path.join(__dirname, '..', 'src', 'data', 'image-manifest.generated.ts')

function sortByNumber(a, b) {
  const numA = parseInt((a.match(/\d+/) || ['0'])[0], 10)
  const numB = parseInt((b.match(/\d+/) || ['0'])[0], 10)
  if (numA !== numB) return numA - numB
  return a.localeCompare(b)
}

/** id поста (папка в lowercase) → массив URL фото по порядку 1, 2, 3... */
const manifest = {}

if (!fs.existsSync(publicImages)) {
  fs.writeFileSync(outPath, `/** Generated. Run npm run update-images to refresh. */\nexport const imageManifest: Record<string, string[]> = {}\n`)
  console.log('No public/images folder, wrote empty map.')
  process.exit(0)
}

const folders = fs.readdirSync(publicImages, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)

for (const folder of folders) {
  const dir = path.join(publicImages, folder)
  const files = fs.readdirSync(dir).filter((f) => IMAGE_EXT.test(f))
  files.sort(sortByNumber)
  const key = folder.toLowerCase()
  manifest[key] = files.map((f) => `/images/${folder}/${f}`)
}

const content = `/** Generated by scripts/build-image-manifest.js. Do not edit. */\nexport const imageManifest: Record<string, string[]> = ${JSON.stringify(manifest, null, 2)}\n`
fs.writeFileSync(outPath, content)
console.log('image-manifest.generated.ts written:', Object.keys(manifest).length, 'folders')
